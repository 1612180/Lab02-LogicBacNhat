tuoi_tac(GV, Tuoi) :-
	ngay_sinh(GV, date(Year, _, _)),
	% lay thoi gian hien tai
	get_time(TimeStamp),
	stamp_date_time(TimeStamp, DateTime, 'UTC'),
	date_time_value(year, DateTime, YearNow),
	Tuoi is YearNow - Year.

tuoi_bang_nhau(GV1, GV2) :-
	tuoi_tac(GV1, Tuoi1),
	tuoi_tac(GV2, Tuoi2),
	GV1 \== GV2,
